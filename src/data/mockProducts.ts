// Function to generate variants for a product
function makeVariants(productId: string, colors: string[], sizes: string[], defaultStock = 10) {
  const list = [];
  let index = 1;
  for (const color of colors) {
    for (const size of sizes) {
      list.push({
        id: `${productId}-v-${index++}`,
        productId,
        color,
        size,
        stock: size === 'GG' ? Math.floor(defaultStock * 0.4) : size === 'PP' ? Math.floor(defaultStock * 0.3) : defaultStock,
      });
    }
  }
  return list;
}

